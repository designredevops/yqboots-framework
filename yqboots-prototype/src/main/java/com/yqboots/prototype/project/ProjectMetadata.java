package com.yqboots.prototype.project;

import org.hibernate.validator.constraints.NotEmpty;

import javax.validation.constraints.NotNull;
import java.io.Serializable;

/**
 * Created by Administrator on 2016-05-28.
 */
@SuppressWarnings("serial")
public class ProjectMetadata implements Serializable {
    public static final String KEY = "metadata";

    @NotEmpty
    private String name;

    @NotNull
    private ProjectType type = ProjectType.MAVEN;

    @NotEmpty
    private String groupId;

    @NotEmpty
    private String artifactId;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public ProjectType getType() {
        return type;
    }

    public void setType(ProjectType type) {
        this.type = type;
    }

    public String getGroupId() {
        return groupId;
    }

    public void setGroupId(String groupId) {
        this.groupId = groupId;
    }

    public String getArtifactId() {
        return artifactId;
    }

    public void setArtifactId(String artifactId) {
        this.artifactId = artifactId;
    }
}
